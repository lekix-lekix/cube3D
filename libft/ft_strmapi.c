#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*tab;

	if (!s)
		return (NULL);
	i = 0;
	tab = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!tab)
		return (0);
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
